    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        int selRow = jTable1.getSelectedRow();
        if (selRow != -1) {// meaning the row is valid
            int column = 0;  // the ID is located at the first column
            String id = jTable1
            .getValueAt(selRow, column).toString();
            int ans = JOptionPane.showConfirmDialog(jTable1,
                "Are you sure you want to delete this item?",
                "Delete Confirmation",
                JOptionPane.YES_NO_OPTION);

            if (ans == JOptionPane.YES_OPTION) {    // YES
                try {
                    Class.forName("com.mysql.jdbc.Driver");// the connector
                    String conURL = "jdbc:mysql://localhost/blaurodb?"
                    + "user=root&password=";
                    Connection con = DriverManager.getConnection(conURL);
                    PreparedStatement pstmt = con.prepareStatement("DELETE FROM products WHERE prdct_id = ? ");
                    pstmt.setString(1, id);
                    pstmt.executeUpdate();

                    refresh();

                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(HOME.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(HOME.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        } else { // meaning if the row is invalid ( -1 )
            JOptionPane.showMessageDialog(null, "Please select item to be deleted",
                "No record selected",
                JOptionPane.WARNING_MESSAGE);

        }         // TODO add your handling code here:
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        // TODO add your handling code here:

        int table_row = jTable1.getSelectedRow();
        this.enableAddProductFields();

        if(table_row != -1){
            id = jTable1.getValueAt(table_row, 0);
            Object product_name = jTable1.getValueAt(table_row, 1);
            Object pro_qty = jTable1.getValueAt(table_row, 2);
            Object pro_price = jTable1.getValueAt(table_row, 3);

            PRODUCTN1.setText((String) product_name);
            PRODUCTQ1.setValue(Integer.valueOf((String) pro_qty));
            PRODUCTP1.setValue(Double.valueOf((String) pro_price));

            addnew.setVisible(true);
            addnew.setLocationRelativeTo(rootPane);
            addnew.setAlwaysOnTop(true);
            save_btn1.setVisible(true);
            ADDBTN1.setVisible(false);
            addqty_btn1.setVisible(false);

            PRODUCTQ1.setEnabled(false);
        }else{
            JOptionPane.showMessageDialog(rootPane, "Please Select a product", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton8ActionPerformed

