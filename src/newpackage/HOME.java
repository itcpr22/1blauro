    private void addqty_btn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addqty_btn1ActionPerformed
        String saqid = aqid1.getText();
        String pn = PRODUCTN1.getText();
        int qty = (int)PRODUCTQ1.getValue();
        int c = JOptionPane.showConfirmDialog(addnew, "Would you like to add\n "+qty+"\n to "+pn+" product?", "Add Quantity", JOptionPane.YES_NO_OPTION);
        if(c == JOptionPane.YES_OPTION){
            try {
                Class.forName("com.mysql.jdbc.Driver");
                com.mysql.jdbc.Connection conn = (com.mysql.jdbc.Connection) DriverManager.getConnection(con.url, con.username, con.password);

                PreparedStatement pstmt = conn.prepareStatement("update products set prdct_quantity = prdct_quantity + ? where prdct_id=? ");
                pstmt.setFloat(1, qty);
                pstmt.setString(2, saqid);

                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(addnew, "You Added Quantity " + qty);
                addnew.setVisible(false);
                this.refresh();
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(HOME.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_addqty_btn1ActionPerformed

